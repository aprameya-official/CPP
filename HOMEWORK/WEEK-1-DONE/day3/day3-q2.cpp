#include <iostream>
using namespace std;

int main() {
    int a, b;

    // Input: Read two integer values 'a' and 'b' from the user
    cin >> a >> b;

    // Calculate the difference between 'a' and 'b'
    int sub = a - b;

    // Output: Display the result of the subtraction
    cout << sub;

    return 0;
}
